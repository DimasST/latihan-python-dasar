print("nilai b %= 3 menjadi nilai :", b)

b = 10
print('nilai b :', b)

b //= 3 # artinya b = b // 3
print("nilai b //= 3 menjadi nilai :", b)

b = 10
print('nilai b :', b)

b **= 3 # artinya b = b ** 3
