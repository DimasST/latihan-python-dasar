# operasi bitwise
# OR (|)
c = True 
print('nilai c :', c)
c |= False
print('nilai c |= False menjadi nilai :', c)
c = False
print('nilai c :', c)
c |= False
print('nilai c |= False menjadi nilai :', c)

# AND (&)
c = True 
print('nilai c :', c)
c &= False
print('nilai c &= False menjadi nilai :', c)
c = False
print('nilai c :', c)
c &= False
print('nilai c &= False menjadi nilai :', c)

# XOR (^)
c = True 
print('nilai c :', c)
c ^= False
print('nilai c ^= False menjadi nilai :', c)
c = False
print('nilai c :', c)
c ^= False
print('nilai c ^= False menjadi nilai :', c)


# LEFT SHIFT (<<)
c = True 
print('nilai c :', c)
c <<= 1
print('nilai c <<= 1 menjadi nilai :', c)
c = False
print('nilai c :', c)
c <<= 1
print('nilai c <<= 1 menjadi nilai :', c)

# RIGHT SHIFT (>>)
c = True 
print('nilai c :', c)
c >>= 1
print('nilai c >>= 1 menjadi nilai :', c)
