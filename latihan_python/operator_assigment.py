# operasi yang dapat dilakukan dengan disingkat
# operasi ditambah dengan assigment

a = 5 # ini adalah assigment
print("nilai a :", a)

a += 2 # artinya a = a + 1
print("nilai a += 2 menjadi nilai :", a)

a -= 2 # artinya a = a - 1
print("nilai a -= 2 menjadi nilai :", a)
a *= 2 # artinya a = a * 2
print("nilai a *= 2 menjadi nilai :", a)

a /= 2 # artinya a = a / 2
print("nilai a /= 2 menjadi nilai :", a)

b = 10
